    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        m = jTextField1.getText();
        mm = jTextField2.getText();
        if("".equals(m) | "".equals(mm)){
            JOptionPane.showMessageDialog(null, "Las casillas o una de las casillas se encuentran vacias");
        }else{
            n = Integer.parseInt(m);
            nn = Integer.parseInt(mm);
            aux = n;
            aux2 = nn;
            do{
                if(n > nn){
                    rest = n - nn;
                    n = rest;
                }else{
                    rest2 = nn - n;
                    nn = rest2;
                }
                if(aux % n == 0 & aux2 % n == 0){
                    mcd = n;
                    if(mcd == 1 ){
                        JOptionPane.showMessageDialog(null, "El Maximo Comun Dvisor es: <" + mcd + "> por lo tanto es primo relativo");
                    }else{
                        JOptionPane.showMessageDialog(null, "El Maximo Comun Dvisor es: <" + mcd + "> por lo tanto no es primo relativo");
                    }
                    break;
                }else if(aux % nn == 0 & aux2 % nn == 0){
                    mcd = nn;
                    if(mcd == 1){
                        JOptionPane.showMessageDialog(null, "El Maximo Comun Dvisor es: <" + mcd + "> por lo tanto es primo relativo");
                    }else{
                        JOptionPane.showMessageDialog(null, "El Maximo Comun Dvisor es: <" + mcd + "> por lo tanto no es primo relativo");
                    }
                    break;
                }
            }while((aux % 2 == 0) | (aux2 % 2 == 0));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

