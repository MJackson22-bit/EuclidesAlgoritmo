    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        m = jTextField1.getText();
        mm = jTextField2.getText();
        if("".equals(m) | "".equals(mm)){
            JOptionPane.showMessageDialog(null, "No hay numeros para realizar la operacion");
        }else{
            n = Integer.parseInt(m);
            nn = Integer.parseInt(mm);
            aux = n;
            aux2 = nn;
            do{
                if(n > nn){
                    rest = n - nn;
                    //JOptionPane.showMessageDialog(null, n + "-" + nn + "=" + rest);
                    jTextArea1.append(n + " - " + nn + " = " + rest + "\n");
                    n = rest;
                }else{
                    rest2 = nn - n;
                   // JOptionPane.showMessageDialog(null, nn + "-" + n + "=" + rest2);
                    jTextArea1.append(nn + " - " + n + " = " + rest2 + "\n");
                    nn = rest2;
                }
                if(aux % n == 0 & aux2 % n == 0){
                    mcd = n;
                    jTextArea1.append("El Maximo Comun Dvisor es: " + mcd);
                    break;
                }else if(aux % nn== 0 & aux2 % nn == 0){
                    mcd = nn;
                    jTextArea1.append("El Maximo Comun Dvisor es: " + mcd);
                    break;
                }
            }while((aux % 2 == 0) | (aux2 % 2 == 0));
        }
    }//GEN-LAST:event_jButton2ActionPerformed

